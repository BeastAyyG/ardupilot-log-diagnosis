import json
import os
import sys


# ─── ANSI colour helpers ──────────────────────────────────────────────────────

def _use_color() -> bool:
    """Return True when stdout is a TTY and NO_COLOR is not set."""
    return sys.stdout.isatty() and os.environ.get("NO_COLOR", "") == ""


_RESET  = "\033[0m"
_BOLD   = "\033[1m"
_RED    = "\033[91m"
_YELLOW = "\033[93m"
_GREEN  = "\033[92m"
_CYAN   = "\033[96m"
_DIM    = "\033[2m"


def _c(text: str, *codes: str) -> str:
    """Wrap *text* with ANSI codes only when color is enabled."""
    if not _use_color():
        return text
    return "".join(codes) + text + _RESET


